// Concatenates the migrations (0001..) into a single paste-ready file for the
// Supabase SQL editor: supabase/schema-bundle.sql. Excludes the test-only shim
// (Supabase provides auth) and the seed (applied separately once keys exist).
//
// Run: node scripts/bundle-schema.mjs   (or: pnpm bundle:schema)

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const migDir = join(root, "supabase", "migrations");
const files = readdirSync(migDir).filter((f) => f.endsWith(".sql")).sort();

const parts = [
  "-- GENERATED by scripts/bundle-schema.mjs — all FirstEmployer migrations in order.",
  "-- Paste into the Supabase SQL editor of a fresh project and run once.",
  "-- Excludes the PGlite test shim (Supabase already provides auth.uid() etc.).",
  "",
];
for (const f of files) {
  parts.push(`-- ============================================================`);
  parts.push(`-- ${f}`);
  parts.push(`-- ============================================================`);
  parts.push(readFileSync(join(migDir, f), "utf8").trimEnd());
  parts.push("");
}

writeFileSync(join(root, "supabase", "schema-bundle.sql"), parts.join("\n"));
console.log(`Wrote supabase/schema-bundle.sql (${files.length} migrations)`);
